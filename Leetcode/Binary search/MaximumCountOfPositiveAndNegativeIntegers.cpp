#include <bits/stdc++.h>
using namespace std;
class Solution {
public:
    int maximumCount(vector<int>& nums) {
        // o(log n)
        // Lower bound
        int upperBound = -1;
        int lowerBound = nums.size();
        int low = 0;
        int high = nums.size()-1;
        while (low <= high) {
            int mid = (low+high)/2;
            if (nums[mid] >= 1) {
                lowerBound = mid;
                high = mid-1;
            }
            else {
                low = mid+1;
            }
        }
        // Upper Bound
        low = 0;
        high = nums.size()-1;
        while (low <= high) {
            int mid = (low+high)/2;
            if (nums[mid] <= -1) {
                upperBound = mid;
                low = mid+1;
            }
            else {
                high = mid-1;
            }
        }
        cout << "LowerBound = " << lowerBound << "\n";
        cout << "UpperBound = " << upperBound << "\n";
        int neg = upperBound+1;
        int pos = nums.size()-lowerBound;
        cout << "Positive elements = " << pos << "\n";
        cout << "Negative Elements = " << neg << "\n";
        return max(pos, neg); 
    }
}obj;

